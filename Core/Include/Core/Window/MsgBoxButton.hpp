#pragma once

namespace Core
{

	enum class MsgBoxButton : long {
		//////////////////////////////////////////////////////////////////////////
		///	The message box contains one push button: OK. This is the
		///	default.
		//////////////////////////////////////////////////////////////////////////
		Ok = 0x00000000L,

		//////////////////////////////////////////////////////////////////////////
		///	The message box contains two push buttons: OK and
		///	Cancel.
		//////////////////////////////////////////////////////////////////////////
		OkCancel = 0x00000001L,

		//////////////////////////////////////////////////////////////////////////
		///	The message box contains three push buttons : Abort, Retry,
		///	and Ignore.
		//////////////////////////////////////////////////////////////////////////
		AbortRetryIgnore  = 0x00000002L,

		//////////////////////////////////////////////////////////////////////////
		///	The message box contains three push buttons: Yes, No, and
		///	Cancel.
		//////////////////////////////////////////////////////////////////////////
		YesNoCancel = 0x00000003L,

		//////////////////////////////////////////////////////////////////////////
		///	The message box contains two push buttons: Yes and No.
		//////////////////////////////////////////////////////////////////////////
		YesNo = 0x00000004L,

		//////////////////////////////////////////////////////////////////////////
		///	The message box contains two push buttons: Retry and
		///	Cancel.
		//////////////////////////////////////////////////////////////////////////
		RetryCancel = 0x00000005L,

		//////////////////////////////////////////////////////////////////////////
		///	The message box contains three push buttons : Cancel, Try
		///	Again, Continue.Use this message box type instead of
		///	MB_ABORTRETRYIGNORE.
		//////////////////////////////////////////////////////////////////////////
		CancelTryContinue = 0x00000006L,

		//////////////////////////////////////////////////////////////////////////
		///	Adds a Help button to the message box. When the user
		///	clicks the Help button or presses F1, the system sends a
		///	WM_HELP message to the owner
		//////////////////////////////////////////////////////////////////////////
		Help = 0x00004000L
	};

}