#pragma once

namespace Core
{

	enum class MsgBoxIcon: long {

		//////////////////////////////////////////////////////////////////////////
		/// A stop - sign icon appears in the message box.
		//////////////////////////////////////////////////////////////////////////
		Error = 0x00000010L,

		//////////////////////////////////////////////////////////////////////////
		/// A stop - sign icon appears in the message box.
		//////////////////////////////////////////////////////////////////////////
		Hand = 0x00000010L,

		//////////////////////////////////////////////////////////////////////////
		/// A question - mark icon appears in the message box.
		/// The question - mark message icon is no longer recommended
		/// because it does not clearly represent a specific type of
		/// message and because the phrasing of a message as a
		/// question could apply to any message type. In addition,
		/// users can confuse the message symbol question mark with
		/// Help information.Therefore, do not use this question mark
		/// message symbol in your message boxes. The system
		/// continues to support its inclusion only for backward
		/// compatibility.
		//////////////////////////////////////////////////////////////////////////
		Question = 0x00000020L,

		//////////////////////////////////////////////////////////////////////////
		/// A stop - sign icon appears in the message box.
		//////////////////////////////////////////////////////////////////////////
		Stop = 0x00000010L,

		//////////////////////////////////////////////////////////////////////////
		/// An exclamation - point icon appears in the message box.
		//////////////////////////////////////////////////////////////////////////
		Exclamation = 0x00000030L,
		
		//////////////////////////////////////////////////////////////////////////
		/// An exclamation - point icon appears in the message box.
		//////////////////////////////////////////////////////////////////////////
		Warning = 0x00000030L,

		//////////////////////////////////////////////////////////////////////////
		/// An icon consisting of a lowercase letter i in a circle appears
		/// in the message box.
		//////////////////////////////////////////////////////////////////////////
		Information = 0x00000040L,
		
		//////////////////////////////////////////////////////////////////////////
		/// An icon consisting of a lowercase letter i in a circle appears
		/// in the message box.
		//////////////////////////////////////////////////////////////////////////
		Asterisk = 0x00000040L,		
	};

}