#pragma once

#include <Core/System/IEventListener.hpp>
#include <Core/Window/WindowEvent.hpp>

namespace Core
{
	////////////////////////////////////////////////////////////
	/// Forward declarations
	////////////////////////////////////////////////////////////
	class Time;

	////////////////////////////////////////////////////////////
	/// \brief Define event listener that is implemented
	///		   by the client
	/// 
	////////////////////////////////////////////////////////////
	class Sketch : public IEventListener<WindowEvent>
	{
	public:

		////////////////////////////////////////////////////////////
		/// \brief Default destructor
		/// 
		////////////////////////////////////////////////////////////
		virtual ~Sketch() override = default;

		////////////////////////////////////////////////////////////
		/// \brief Load asset files
		/// 
		////////////////////////////////////////////////////////////
		virtual bool OnPreload();

		////////////////////////////////////////////////////////////
		/// \brief Initialize variables / setup environment for drawing
		/// 
		////////////////////////////////////////////////////////////
		virtual void OnSetup();

		////////////////////////////////////////////////////////////
		/// \brief Destroys the content/deletes pointers etc.
		/// 
		////////////////////////////////////////////////////////////
		virtual void OnDestroy();

		////////////////////////////////////////////////////////////
		/// \brief Update variables & draw to screen
		///
		///	\param deltaTime Time elapsed since last frame in
		///					 seconds.
		/// 
		////////////////////////////////////////////////////////////
		virtual void OnDraw(float deltaTime);

		////////////////////////////////////////////////////////////
		/// \brief Render ImGui content
		///
		////////////////////////////////////////////////////////////
		virtual void OnDrawGui();

		////////////////////////////////////////////////////////////
		/// \brief OnEvent callback for window events. This method
		///		   does nothing by default
		/// 
		////////////////////////////////////////////////////////////
		virtual void OnEvent(const WindowEvent& event) override;

	};
}